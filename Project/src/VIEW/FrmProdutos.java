    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            ProdutosDTO objProdutoDTO = new ProdutosDTO();
            ProdutosDAO objProdutoDAO = new ProdutosDAO();

            objProdutoDTO.setNomePizza(txtNome.getText());
            objProdutoDTO.setDescricao(txtDesc.getText());

            if(CbxTamanho.getSelectedIndex() != 0) {
                String value = CbxTamanho.getSelectedItem().toString();
                objProdutoDTO.setTamPizza(value);
            } else{
                JOptionPane.showMessageDialog(null, "Escolha outra opção!");
            }
            
            objProdutoDTO.setBebida(txtBebida.getText());
            objProdutoDTO.setAdicional(txtAdicional.getText());
            objProdutoDTO.setPreco(Float.parseFloat(txtPreco.getText()));

            objProdutoDAO.CadastrarProdutos(objProdutoDTO);
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            ListarValoresProdutos();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro! Não foi possível cadastrar!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


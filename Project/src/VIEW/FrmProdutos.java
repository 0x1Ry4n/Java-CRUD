     private void ListarValoresProdutos() {
        try {
            ProdutosDAO objProdutosDAO = new ProdutosDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();
            model.setNumRows(0);

            ArrayList<ProdutosDTO> lista = objProdutosDAO.PesquisarProduto();

            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getId_Cardapio(),
                    lista.get(i).getNomePizza(),
                    lista.get(i).getDescricao(),
                    lista.get(i).getTamPizza(),
                    lista.get(i).getBebida(),
                    lista.get(i).getAdicional(),
                    lista.get(i).getPreco()
                });
            }
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Listar valores VIEW: " + error);
        }
    }
     
     
     private void CarregarCampos() {
        try {
            int setar = tabelaProdutos.getSelectedRow();
            txtId.setText(tabelaProdutos.getModel().getValueAt(setar, 0).toString());
            txtNome.setText(tabelaProdutos.getModel().getValueAt(setar, 1).toString());
            txtDesc.setText(tabelaProdutos.getModel().getValueAt(setar, 2).toString());
            CbxTamanho.setSelectedItem(tabelaProdutos.getModel().getValueAt(setar, 3).toString());
            txtBebida.setText(tabelaProdutos.getModel().getValueAt(setar, 4).toString());
            txtAdicional.setText(tabelaProdutos.getModel().getValueAt(setar, 5).toString());
            txtPreco.setText(tabelaProdutos.getModel().getValueAt(setar, 6).toString());
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Erro: " + error.getMessage());
        }
    }
     
     
    private void AlterarProduto() {
        try {
            int id_produto;
            String nome, descricao, bebida, adicional, tamanho;
            float preco;

            id_produto = Integer.parseInt(txtId.getText());
            nome = txtNome.getText();
            descricao = txtDesc.getText();
            bebida = txtBebida.getText();
            adicional = txtAdicional.getText();
            preco = Float.parseFloat(txtPreco.getText());
            tamanho = String.valueOf(CbxTamanho.getSelectedItem());

            ProdutosDTO objProdutoDTO = new ProdutosDTO();
            objProdutoDTO.setId_Cardapio(id_produto);
            objProdutoDTO.setNomePizza(nome);
            objProdutoDTO.setDescricao(descricao);
            objProdutoDTO.setTamPizza(tamanho);
            objProdutoDTO.setBebida(bebida);
            objProdutoDTO.setAdicional(adicional);
            objProdutoDTO.setPreco(preco);

            ProdutosDAO objProdutoDAO = new ProdutosDAO();
            objProdutoDAO.alterarProduto(objProdutoDTO);
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro! Não foi possível alterar!");
        }
    }
    
    
    private void excluirProduto() {
        int id_produto; 
        id_produto = Integer.parseInt(txtId.getText());
        ProdutosDTO objProdutoDTO = new ProdutosDTO();
        ProdutosDAO objProdutoDAO = new ProdutosDAO();
        
        objProdutoDTO.setId_Cardapio(id_produto);
        
        objProdutoDAO.excluirProduto(objProdutoDTO);
    }
    

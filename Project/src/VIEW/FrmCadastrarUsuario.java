    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String usuario, senha;
            FrmUsuario objUsuario = new FrmUsuario();
            CadastrarUsuarioDTO objCadastrarUsuarioDTO = new CadastrarUsuarioDTO();
            CadastrarUsuarioDAO objUsuarioDAO = new CadastrarUsuarioDAO();
            
            usuario = txtusuario.getText();
            senha = txtsenha.getText();
            
            if("".equals(usuario) || "".equals(senha)) {
                JOptionPane.showMessageDialog(null, "Erro! Preencha todos os campos!");
            } else {
                objCadastrarUsuarioDTO.setUsuario(Criptografia.criptografarUsuario(usuario));
                objCadastrarUsuarioDTO.setSenha(Criptografia.criptografarSenha(senha));
                objUsuarioDAO.cadastrarUsuario(objCadastrarUsuarioDTO);
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                dispose();
                objUsuario.setVisible(true);
            }
        } catch(Exception err) {
            JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel cadastrar!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


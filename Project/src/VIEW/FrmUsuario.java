    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        try {
            String usuario, senha;
            UsuarioDTO objUsuarioDTO = new UsuarioDTO();
            UsuarioDAO objUsuarioDAO = new UsuarioDAO();
            FrmPrincipal objFrmPrincipal = new FrmPrincipal();
            usuario = txtusuario.getText();
            senha = txtsenha.getText();
            if("".equals(usuario) || "".equals(senha)) {
                JOptionPane.showMessageDialog(null, "Erro! Preencha algum campo!");
            }else {
                objUsuarioDTO.setUsuario(Criptografia.criptografarUsuario(usuario));
                objUsuarioDTO.setSenha(Criptografia.criptografarSenha(senha));
                ResultSet result = objUsuarioDAO.Autenticacao(objUsuarioDTO);
                if (result.next()) {
                    dispose();
                    objFrmPrincipal.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Usu√°rio ou senha inv√°lidos");
                }
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Erro: " + err.getMessage());
        }
    }//GEN-LAST:event_btnEntrarActionPerformed


    private void ListarValoresClientes() {
        try {
            ClientesDAO objClienteDAO = new ClientesDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaCliente.getModel();
            model.setNumRows(0);

            ArrayList<ClientesDTO> lista = objClienteDAO.PesquisarCliente();

            for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[]{
                    lista.get(i).getId_Cliente(),
                    lista.get(i).getNomeCliente(),
                    lista.get(i).getCpfCli(),
                    lista.get(i).getTelefone(),
                    lista.get(i).getBairro(),
                    lista.get(i).getComplemento(),
                    lista.get(i).getNumeroCasa()
                });
            }
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Listar valores VIEW: " + error);
        }
    }
     
     
    private void CarregarCampos() {
        try {
            int setar = tabelaCliente.getSelectedRow();
            txtId.setText(tabelaCliente.getModel().getValueAt(setar, 0).toString());
            txtNome.setText(tabelaCliente.getModel().getValueAt(setar, 1).toString());
            txtCPF.setText(tabelaCliente.getModel().getValueAt(setar, 2).toString());
            txtTel.setText(tabelaCliente.getModel().getValueAt(setar, 3).toString());
            txtBairro.setText(tabelaCliente.getModel().getValueAt(setar, 4).toString());
            txtComplemento.setText(tabelaCliente.getModel().getValueAt(setar, 5).toString());
            txtCasa.setText(tabelaCliente.getModel().getValueAt(setar, 6).toString());
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Erro: " + error.getMessage());
        }
    }
     
     
    private void AlterarCliente() {
        try {
            int id_cliente, numeroCasa;
            String nome, cpf, telefone, bairro, complemento;

            id_cliente = Integer.parseInt(txtId.getText());
            nome = txtNome.getText();
            cpf = txtCPF.getText();
            telefone = txtTel.getText();
            bairro = txtBairro.getText();
            complemento = txtComplemento.getText();
            numeroCasa = Integer.parseInt(txtCasa.getText());


            ClientesDTO objClienteDTO = new ClientesDTO();
            objClienteDTO.setId_Cliente(id_cliente);
            objClienteDTO.setNomeCliente(nome);
            objClienteDTO.setCpfCli(cpf);
            objClienteDTO.setTelefone(telefone);
            objClienteDTO.setBairro(bairro);
            objClienteDTO.setComplemento(complemento);
            objClienteDTO.setNumeroCasa(numeroCasa);

            ClientesDAO objClienteDAO = new ClientesDAO();
            objClienteDAO.alterarCliente(objClienteDTO);
            JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro! NÃ£o foi possÃ­vel alterar!");
        }
    }
    
    
    private void excluirCliente() {
        int id_cliente; 
        id_cliente = Integer.parseInt(txtId.getText());
        ClientesDTO objClienteDTO = new ClientesDTO();
        ClientesDAO objClienteDAO = new ClientesDAO();
        
        objClienteDTO.setId_Cliente(id_cliente);
        
        objClienteDAO.excluirCliente(objClienteDTO);
    }


    
    

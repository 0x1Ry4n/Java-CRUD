    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            ClientesDAO objClienteDAO = new ClientesDAO();
            ClientesDTO objClienteDTO = new ClientesDTO();

            objClienteDTO.setNomeCliente(txtNome.getText());
            objClienteDTO.setCpfCli(txtCPF.getText());
            objClienteDTO.setTelefone(txtTel.getText());
            objClienteDTO.setBairro(txtBairro.getText());
            objClienteDTO.setComplemento(txtComplemento.getText());
            objClienteDTO.setNumeroCasa(Integer.parseInt(txtCasa.getText()));

            objClienteDAO.CadastrarCliente(objClienteDTO);
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            ListarValoresClientes();
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro! nÃ£o foi possÃ­vel cadastrar!");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

